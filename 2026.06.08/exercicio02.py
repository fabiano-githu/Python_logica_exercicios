'''
Questão 2 – Identificando tipos de dados
Crie variáveis contendo:

 - Um texto
 - Um número inteiro
 - Um número decimal
 - Um valor booleano

 Depois, utilize a função type() para exibir o tipo de cada variável.

Exemplo de saída:
```
<class 'str'>
<class 'int'>
<class 'float'>
<class 'bool'>
```
'''

# Um texto
name = "Joca"

# Um número inteiro
age = 35

# Um número decimal (float)
height = 1.78

# Um valor booleano
is_student = True

# Depois, utilize a função type() para exibir o tipo de cada variável.
print("name", type(name))
print("age", type(age))
print("height", type(height))
print("is_student", type(is_student))

