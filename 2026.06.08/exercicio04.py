'''
Questão 4 – Verificação de maioridade
Crie uma variável chamada idade.

Utilize uma estrutura `if...else` para verificar:

 - Se a idade for maior ou igual a 18, exiba: "Maior de idade"
 - Caso contrário: "Menor de idade"
'''

# Crie uma variável chamada idade.
idade = 17

# Utilize uma estrutura `if...else` para verificar:
# Se a idade for maior ou igual a 18, exiba: "Maior de idade"
if idade >= 18:
    print("Maior de idade")
# Caso contrário: "Menor de idade"
else:
    print("Menor de idade")